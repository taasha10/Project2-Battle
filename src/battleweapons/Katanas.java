package battleweapons;

/**
 * Represents Katanas. They are lightweight curved swords that come in pairs. They can do a base
 * of 4-6 points of damage when they hit. They are so light that a player can carry two of them
 * (which attack separately).
 */
public class Katanas extends Swords {

  Katanas(String weaponName) {
    super(weaponName, "Katanas");
  }
}
